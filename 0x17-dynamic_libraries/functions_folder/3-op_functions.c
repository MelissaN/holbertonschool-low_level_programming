/**
 * add - add
 * @a: integer
 * @b: integer
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract
 * @a: integer
 * @b: integer
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply
 * @a: integer
 * @b: integer
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide
 * @a: integer
 * @b: integer
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus for remainder
 * @a: integer
 * @b: integer
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
